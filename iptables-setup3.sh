#!/bin/sh
#--------------------------------------------------------------------
#iptables-setup3
# ファイアウォール初期設定スクリプト
# アクセス拒否IPアドレスの登録を行う
#----------------------------------------
# 初期設定
#----------------------------------------
#アクセス拒否IP累積リスト
denyIpFile=/var/log/apache2/denyIP
#----------------------------------------
#累積リストと今回のリストを比較して新規をiptablesに登録
for inIp in `$denyIpFile`;
do
   iptables -I INPUT -s $inIp -j DROP
done
